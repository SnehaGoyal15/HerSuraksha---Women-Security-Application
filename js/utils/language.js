function preserveWhitespace(original, translated) {
    const leading = original.match(/^\s*/)?.[0] || "";
    const trailing = original.match(/\s*$/)?.[0] || "";
    return `${leading}${translated}${trailing}`;
}

export function getCurrentLanguage() {
    return localStorage.getItem(LANGUAGE_KEY) || "en";
}

export function setCurrentLanguage(language) {
    localStorage.setItem(LANGUAGE_KEY, language);
}

export function t(text) {
    const language = getCurrentLanguage();
    if (language === "en") return text;
    return translations[language]?.[text] || text;
}

export function translateTemplate(key, values = {}) {
    const language = getCurrentLanguage();
    const template = templateTranslations[language]?.[key] || templateTranslations.en[key] || key;
    return Object.entries(values).reduce(
        (result, [token, value]) => result.replaceAll(`{${token}}`, value),
        template
    );
}

export function alertT(message) {
    alert(t(message));
}

export function confirmT(message) {
    return confirm(t(message));
}

export function promptT(message, defaultValue = "") {
    return prompt(t(message), defaultValue);
}

export function applyTranslations(root = document) {
    const language = getCurrentLanguage();
    const scope = root.body || root;
    document.documentElement.lang = language === "hi" ? "hi" : "en";
    document.title = t(document.title);

    if (language === "en") return;

    const walker = document.createTreeWalker(scope, NodeFilter.SHOW_TEXT);
    const textNodes = [];

    while (walker.nextNode()) {
        const node = walker.currentNode;
        if (!node.parentElement) continue;
        const parentTag = node.parentElement.tagName;
        if (["SCRIPT", "STYLE", "TEXTAREA"].includes(parentTag)) continue;
        if (!node.nodeValue || !node.nodeValue.trim()) continue;
        textNodes.push(node);
    }

    textNodes.forEach((node) => {
        const original = node.nodeValue;
        const trimmed = original.trim();
        const translated = t(trimmed);
        if (translated !== trimmed) {
            node.nodeValue = preserveWhitespace(original, translated);
        }
    });

    scope.querySelectorAll("[placeholder]").forEach((element) => {
        const placeholder = element.getAttribute("placeholder");
        if (!placeholder) return;
        const translated = t(placeholder);
        if (translated !== placeholder) {
            element.setAttribute("placeholder", translated);
        }
    });

    scope.querySelectorAll("[aria-label]").forEach((element) => {
        const label = element.getAttribute("aria-label");
        if (!label) return;
        const translated = t(label);
        if (translated !== label) {
            element.setAttribute("aria-label", translated);
        }
    });

    scope.querySelectorAll("option").forEach((option) => {
        const translated = t(option.textContent.trim());
        if (translated !== option.textContent.trim()) {
            option.textContent = translated;
        }
    });
}
